#include "utilities_version_class.h"

///==================================================================================================================================================================================
///	Конструктор и деструктор класса
///==================================================================================================================================================================================
utilities_version_class::utilities_version_class()
{
	this->software_name = "UTILITIES_SV";
	this->software_file_name = "libutilites_sv.dll";
	this->software_description = "Common utilities library.";
	this->software_version = "1.0.0.1";
	this->software_copyright = "Vladimir Svetlov";
	this->software_date = "05.07.2021";
}

utilities_version_class::~utilities_version_class()
{
}


///==================================================================================================================================================================================
///	Получение параметров библиотеки
///==================================================================================================================================================================================

std::string utilities_version_class::GetName()
{ return this->software_name; }

std::string utilities_version_class::GetFileName()
{ return this->software_file_name; }

std::string utilities_version_class::GetDescription()
{ return this->software_description; }

std::string utilities_version_class::GetVersion()
{ return this->software_version; }

std::string utilities_version_class::GetCopyright()
{ return this->software_copyright; }

std::string utilities_version_class::GetDate()
{ return this->software_date; }


